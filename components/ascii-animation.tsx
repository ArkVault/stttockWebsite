const INTERVALS: Record<string, number> = {
  pos:      900,
  brain:    750,
  bell:     850,
  calendar: 1100,
  chart:    950,
  building: 1050,
  percent:  900,
  webhook:  800,
};

export function AsciiAnimation({ icon }: { icon: string }) {
  const frames = FRAMES[icon] ?? FRAMES.pos;
  const interval = INTERVALS[icon] ?? 900;
  const [frameIdx, setFrameIdx] = useState(0);

  useEffect(() => {
    const id = setInterval(
      () => setFrameIdx((f) => (f + 1) % frames.length),
      interval
    );
    return () => clearInterval(id);
  }, [frames.length, interval]);

  return (
    <div
      style={{
        width: "100%",
        height: "100%",
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        background: "linear-gradient(160deg, #0d1117 0%, #0f172a 60%, #0d1117 100%)",
        overflow: "hidden",
      }}
    >
      <pre
        className="ascii-shimmer"
        style={{
          fontFamily: "'SF Mono', 'Fira Code', 'Fira Mono', 'Roboto Mono', monospace",
          fontSize: "10px",
          lineHeight: "1.55",
          margin: 0,
          padding: "6px 4px",
          userSelect: "none",
          whiteSpace: "pre",
          letterSpacing: "0.02em",
        }}
      >
        {frames[frameIdx].join("\n")}
      </pre>

      <style jsx>{`
        @keyframes ascii-shimmer {
          0%   { background-position: 200% center; }
          100% { background-position: -200% center; }
        }

        .ascii-shimmer {
          background: linear-gradient(
            90deg,
            #4b5563 0%,
            #9ca3af 18%,
            #7dd3fc 32%,
            #bfdbfe 45%,
            #e0f2fe 50%,
            #bfdbfe 55%,
            #7dd3fc 68%,
            #9ca3af 82%,
            #4b5563 100%
          );
          background-size: 200% auto;
          background-clip: text;
          -webkit-background-clip: text;
          color: transparent;
          animation: ascii-shimmer 5s linear infinite;
        }
      `}</style>
    </div>
  );
}
